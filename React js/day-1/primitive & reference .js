// Primitive Types
let num1 = 10;
let num2 = num1; 

console.log(num1); 
console.log(num2);

num2 = 20;

console.log(num1); 
console.log(num2);

// Reference Types
const obj1 = { value: 30 };
const obj2 = obj1; 

console.log(obj1.value); 
console.log(obj2.value);

obj2.value = 40;

console.log(obj1.value); 
console.log(obj2.value); 
